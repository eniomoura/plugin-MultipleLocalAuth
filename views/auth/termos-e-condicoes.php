<h1>Termo de Uso</h1>
<p> Cultural do Ceará </p> 


<p> Atenção: Leia atentamente os termos abaixo antes de utilizar este site. </p>

<p> O Mapa Cultural do Ceará é um serviço interativo disponibilizado por meio de página eletrônica na internet que oferece informações sobre cultura, agentes cadastrados, serviços, chamadas, editais, espaços culturais, sociais e públicos etc. O acesso ao Mapa representa a aceitação expressa e irrestrita dos termos de uso abaixo descritos. Se você não concorda com os termos não utilize este site.</p>

Introdução

<p> Este Termo de Uso descreve as condições de uso do serviço Mapa Cultural do Ceará, incluindo os direitos e deveres de seus usuários e a política de uso de seus dados pessoais.</p>

<p> O Mapa Cultural do Ceará é uma plataforma livre, gratuita e colaborativa de mapeamento da Secretaria da Cultura do Estado do Ceará (SECULTCE) sobre o cenário cultural cearense. No âmbito da política da Governança Digital da SECULTCE o serviço é operado pela Célula de Tecnologia da Informação e Comunicação (CETIC).</p>

<p> O serviço Mapa Cultural do Ceará é gratuito e sua utilização é uma faculdade do cidadão que, ao utilizá-lo, adere aos termos aqui dispostos, concordando e se submetendo integralmente às suas condições.</p>


<p> Serviços disponíveis através do Mapa Cultural do Ceará</p>

<p> Novos serviços poderão ser disponibilizados a qualquer momento e os serviços existentes poderão ser removidos mediante comunicação prévia aos seus usuários através do site http://www.secult.ce.gov.br </p>


<p> Deveres do usuário do Mapa Cultural do Ceará</p>

<p> A utilização do Mapa Cultural do Ceará somente pode se dar para propósitos lícitos e de modo a não afetar os direitos ou possibilidades de uso do sistema por terceiros. Não serão toleradas condutas ilícitas, imorais ou que possam molestar ou ser inconvenientes para os administradores do sistema, para os serviços disponibilizados ou para terceiros. Da mesma forma, o Mapa Cultural do Ceará não pode ser utilizado para difamar, abusar, assediar, perseguir, ameaçar ou violar quaisquer direitos individuais ou para a transmissão de conteúdo ilícito.</p>

<p> O usuário tem responsabilidade pessoal sobre suas informações de acesso ao Mapa Cultural do Ceará (login, senha, token, certificado digital, frase de acesso ou outros) e deverá mantê-las em segurança, estritamente sob seu controle pessoal, não franqueando, em nenhuma circunstância, seu acesso a terceiros.</p>

<p> O usuário é responsável pela veracidade e pela atualização dos dados que informar ao Mapa Cultural do Ceará, bem como aos serviços disponíveis através dele. O fornecimento de informação falsa poderá causar desde resultados inexatos e indesejados em relação aos serviços, bem como suspender ou cancelar o acesso ao sistema e ainda poderá punir o responsável pelos seus atos nas áreas cíveis, administrativas e penais, conforme a legislação aplicável ao caso.</p>

<p> O usuário deverá comunicar imediatamente ao suporte do Mapa Cultural do Ceará, através do email mapa@secult.ce.gov.br acerca de qualquer suspeita de acesso indevido ao sistema, de falha de segurança ou do comprometimento de suas informações de acesso.</p>

<p> A não observância pelo usuário, dos seus deveres previstos nestes Termos de Uso ou em qualquer outra legislação aplicável, poderá acarretar na suspensão ou no cancelamento do seu acesso ao Mapa Cultural do Ceará, ou a um ou mais serviços disponibilizados, além das outras sanções previstas em Lei.</p>


<p> Direitos do usuário do Mapa Cultural do Ceará e responsabilidades de seus administradores</p>

<p> A SECULTCE é responsável pela segurança das informações coletadas, armazenadas e processadas pelo Mapa Cultural do Ceará, bem como pela segurança das informações transmitidas aos serviços disponibilizados através do Mapa Cultural do Ceará.</p>

<p> A SECULTCE utilizará as medidas adequadas, de acordo com o estado da tecnologia, para garantir a segurança e integridade das informações pessoais sob sua custódia e evitar qualquer modalidade de acesso indevido.</p>

<p> O acesso às informações pessoais dos usuários não poderá ser comercializados e somente poderá ser efetuados por servidores autorizados pela SECULTCE ou por ordem judicial. Os acessos às informações pessoais serão auditados e monitorados pela SECULTCE.</p>

<p> Com o intuito de assegurar a liberdade de expressão e impedir a censura a SECULTCE segue os princípios e garantias do Marco Civil da Internet, Lei nº 12.965, de 23 de abril de 2014.</p>

<p> A SECULTCE poderá suspender a veiculação de informações e/ou dados por fundado receio referente à ilegalidade ou uso indevido de direitos de terceiros. </p>


<p> Política de uso de dados pessoais</p>

<p> O usuário do Mapa Cultural do Ceará será informado previamente sobre quais dados pessoais e obrigatórias serão repassados à SECULTCE.</p>

<p> As informações pessoais fornecidas pelo usuário e tratadas pelo Mapa Cultural do Ceará serão utilizadas para o fornecimento do serviço, com suas funcionalidades descritas nestes Termos de Uso.</p>

<p> Estas informações poderão ser utilizadas pela SECULTCE ou transferidas a outros Órgãos do Governo, sempre que houver o requerimento formal por parte destes e autorização do usuário e, por último, quando a legislação o autorizar.</p>


<p> Condições gerais</p>

<p> Ao utilizar o Mapa Cultural do Ceará, o usuário será informado previamente sobre quais as informações pessoais serão de visualização pública e quais são privadas. Todas as informações públicas poderão ser visualizadas por qualquer cidadão, independente de este estar cadastrado no Mapa Cultural do Ceará.</p>

<p> As informações do usuário do Mapa Cultural do Ceará somente poderão ser modificadas através do sítio web do sistema, mediante o uso de suas informações de acesso.</p>

<p> Caso o usuário tenha perdido as suas informações de acesso, ele poderá utilizar o serviço de recuperação deste, disponibilizados no site do Mapa Cultural do Ceará ou através da equipe técnica no email mapa@secult.ce.gov.br</p>


<p> Suspensão ou término da disponibilização do Mapa Cultural do Ceará</p>

<p> O Mapa Cultural do Ceará poderá ser suspenso ou ter sua disponibilização cessada a qualquer momento, mediante comunicação prévia, aos seus usuários.</p>


