<p> Alteração dos termos de uso</p>

<p> As condições especificadas nestes Termos de Uso poderão ser modificadas a qualquer momento. Caso estas sejam significativas ou afetem, de forma concreta, os direitos e deveres dos usuários do sistema, serão apresentadas ao usuário, de forma clara, na próxima vez em que realizar acesso, momento no qual lhe será questionada a concordância com os novos termos antes que continue utilizando o sistema.</p>

